#include <stdio.h>
#include <stdbool.h>  // Required for boolean variables

int main() {
    // Boolean variables
    bool isPassed = true;
    bool isFailed = false;

    // Printing boolean values
    printf("isPassed: %d\n", isPassed);  // 1 means true
    printf("isFailed: %d\n", isFailed);  // 0 means false
    
    return 0;
}
